# IF Statement

quest= input("Red Pill or Blue Pill? ")

# if elif else statement


if quest == "red" :
    print("You chose the red pill. You'll die now. Womp Womp")
elif quest == "blue" :
    print("So you chose the blue pill... You'll die now. Bye Bye.")
elif quest == "green" :
    print("I asked for either red or blue, not green you genius. Enjoy being dead.")
else: print("I didn't get what you typed, which means you'll now die. Wasn't a pleasure seeing you. Buh-BYe")
