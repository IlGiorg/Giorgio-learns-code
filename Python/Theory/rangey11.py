# Range will create list of numbers from and including the first number but it will not print the last number in the range

number = range(1,11) #here 1 will be printed out but 11 won't be

for x in number: # x is used as a general variable, but it can be changed according to needs
    print(x) 

# X represents everything in the list so it could also be viewed as "for everything in number"

# the range function can also be used directly, without declaring the variable beforehand

for y in range(1,11):
    print (y)